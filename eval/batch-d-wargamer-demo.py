def main():
    print("[loading deps]", file=sys.stderr, flush=True)
    import torch
    from transformers import AutoModelForCausalLM, AutoTokenizer, BitsAndBytesConfig
    from peft import PeftModel

    BASE = "Qwen/Qwen2.5-7B-Instruct"
    ADAPTER = "lerugray/hammerstein-7b-lora"

    print("[loading tokenizer]", file=sys.stderr, flush=True)
    tok = AutoTokenizer.from_pretrained(BASE)

    print("[loading base model in 4-bit]", file=sys.stderr, flush=True)
    bnb = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.bfloat16,
    )
    base = AutoModelForCausalLM.from_pretrained(
        BASE,
        quantization_config=bnb,
        device_map="auto",
        torch_dtype=torch.bfloat16,
    )

    print("[applying LoRA adapter]", file=sys.stderr, flush=True)
    model = PeftModel.from_pretrained(base, ADAPTER)
    model.eval()

    def generate(text: str, max_new: int = 900) -> tuple[str, int]:
        msgs = [{"role": "user", "content": text}]
        prompt_text = tok.apply_chat_template(msgs, tokenize=False, add_generation_prompt=True)
        inputs = tok(prompt_text, return_tensors="pt").to("cuda")
        t0 = time.perf_counter()
        with torch.no_grad():
            out = model.generate(
                **inputs,
                max_new_tokens=max_new,
                temperature=0.0,
                do_sample=False,
                pad_token_id=tok.eos_token_id,
            )
        latency_ms = int((time.perf_counter() - t0) * 1000)
        response = tok.decode(out[0][inputs["input_ids"].shape[1]:], skip_special_tokens=True).strip()
        return response, latency_ms

    results = []
    for s in SCENARIOS:
        print(f"[{s['id']} hammerstein-7b]", file=sys.stderr, flush=True)
        resp_h, lat_h = generate(s["prompt"])
        results.append({
            "id": s["id"], "label": s["label"], "prompt": s["prompt"],
            "cell": "hammerstein-7b", "response": resp_h, "latency_ms": lat_h,
        })

        print(f"[{s['id']} raw-qwen25-7b]", file=sys.stderr, flush=True)
        with model.disable_adapter():
            resp_r, lat_r = generate(s["prompt"])
        results.append({
            "id": s["id"], "label": s["label"], "prompt": s["prompt"],
            "cell": "raw-qwen25-7b", "response": resp_r, "latency_ms": lat_r,
        })

    json.dump(results, sys.stdout, indent=2, ensure_ascii=False)
    print("\n[done]", file=sys.stderr, flush=True)


if __name__ == "__main__":
    main()
